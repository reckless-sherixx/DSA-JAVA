package BitManipulation;

public class BitManipulation {
    public static void main(String[] args) {
        //AND
        System.out.println((5 & 6));
        //OR
        System.out.println((5 | 6));
        //XOR
        System.out.println((5 ^ 6));
        //1's complement
        System.out.println((~5));
    }
}
